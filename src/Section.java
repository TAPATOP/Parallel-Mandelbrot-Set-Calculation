public class Section {
    public Section(int startHeight, int endHeight, int startWidth, int endWidth) {
        this.startHeight = startHeight;
        this.endHeight = endHeight;
        this.startWidth = startWidth;
        this.endWidth = endWidth;
    }

    // Member variables //
    int startHeight;
    int endHeight;
    int startWidth;
    int endWidth;
}
